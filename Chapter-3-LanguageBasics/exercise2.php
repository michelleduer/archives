<?php

$x = 1;
$y = 4;

// test whether the first value equals the second value
echo ($x == $y) . "<br/>";

// test whether the first value is greater than the second value
echo ($x > $y) . "<br/>";

// test whether the first value is less than or equal to the second value
echo ($x <= $y) . "<br/>";

// test whether the first value is not equal to the second value
echo (!($x == $y)) . "<br/>";

?>
